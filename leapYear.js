function isLeapYear(y){
    if (y % 4 === 0) {
        console.log(y + " is a leap year.");
    }
    else{
        console.log(y + " is NOT a leap year")
    }
}
isLeapYear(2016);
isLeapYear(2017);
isLeapYear(2019);
isLeapYear(2020);
isLeapYear(2021);
isLeapYear(2022);
isLeapYear(2023);
isLeapYear(2024);